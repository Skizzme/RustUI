var script = Script.initialize(
    "Example Script",
    ["Rythem", "Skizzme"],
    "1.0.0"
)

var module = ModuleHandler.create("Example Module", "Does nothing lol?", null, Category.COMBAT)
var mode = Settings.createMode("label", "Desc", "Default", ["Default", "Other"])
var slider = Settings.createNumber("another label", "a value", 1.0, 0.0, 2.0, 0.1)
module.registerSettings(mode, slider)

module.onUpdate(function() {
    var entities = World.entities()

    for (var i = 0; i < entities.length; i++) {
        var entity = entities[i]

        if (entity != null) {
            System.print(entity.getName())
        }
    }
})

script.onLoad(function() {
    ModuleHandler.register(module)
    System.print("loaded...")
});

script.onUnload(function() {
    ModuleHandler.unregister(module)
    System.print("unloaded...")
});
